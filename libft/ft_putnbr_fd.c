#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	nb;

	if (n >= 0 && n / 10 == 0)
		ft_putchar_fd((nb = 48 + (n % 10)), fd);
	else if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else if (n < 0)
	{
		n = -n;
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(n, fd);
	}
	else
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
}
